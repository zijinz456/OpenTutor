let currentLocale: Locale = "en";

export function setLocale(locale: Locale) {
  currentLocale = locale;
  if (typeof window !== "undefined") {
    localStorage.setItem("opentutor-locale", locale);
  }
}

const SUPPORTED_LOCALES: Locale[] = ["en", "zh", "ja", "ko", "es", "fr"];

export function getLocale(): Locale {
  if (typeof window !== "undefined") {
    const saved = localStorage.getItem("opentutor-locale") as Locale | null;
    if (saved && SUPPORTED_LOCALES.includes(saved)) {
      currentLocale = saved;
    }
  }
  return currentLocale;
}

export function t(key: string): string {
  return translations[currentLocale]?.[key] ?? translations.en[key] ?? key;
}

export function initLocale() {
  if (typeof window !== "undefined") {
    const saved = localStorage.getItem("opentutor-locale") as Locale | null;
    if (saved) {
      currentLocale = saved;
    } else {
      // Auto-detect from browser
      const browserLang = navigator.language.toLowerCase();
      if (browserLang.startsWith("zh")) currentLocale = "zh";
      else if (browserLang.startsWith("ja")) currentLocale = "ja";
      else if (browserLang.startsWith("ko")) currentLocale = "ko";
      else if (browserLang.startsWith("es")) currentLocale = "es";
      else if (browserLang.startsWith("fr")) currentLocale = "fr";
      else currentLocale = "en";
    }
  }
}
